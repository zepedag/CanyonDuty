        private void GenerateIMG()
        {
            char v;
            Graphics g;

            // map properties     
            size = new Size(256, 22);   // 256 columns 22 rows
            BMP = new Bitmap(size.Width * Unit, size.Height * Unit);
            g = Graphics.FromImage(BMP);

            for (int y = 0; y < size.Height; y++)
            {
                for (int x = 0; x < size.Width; x++)
                {
                    v = map[y * size.Width + x];
                    switch (v)
                    {
                        case '.':
                            //Brick.DrawTomatoBrick(g, x, y, Unit);
                            break;
                        case '*':
                            //Brick.DrawYellowBrick(g, x, y, Unit);
                            break;
                        case '#':
                            Brick.DrawGreenBrick(g, x, y, Unit);
                            break;
                        case 'o':
                            g.FillRectangle(Brushes.Blue, x * Unit, y * Unit, Unit, Unit);
                            break;
                    }
                    g.DrawRectangle(Pens.Gray, x * Unit, y * Unit, Unit, Unit);
                }
            }
        }


